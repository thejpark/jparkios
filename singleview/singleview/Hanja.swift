func getHanjaData(hangul: String, hanja:String) -> Hanja {

    var p: [Hanja] = getHanjaDataFromHangul(hangul)

    if  hangul == "이" {
        p = p + getHanjaDataFromHangul("리")
    }
    else if  hangul == "임" {
        p = getHanjaDataFromHangul("림") + p
    }
    else if hangul == "육" {
        p = getHanjaDataFromHangul("륙") + p
    }
    else if hangul == "유" {
        p = p + getHanjaDataFromHangul("류")
    }
    else if hangul == "용" {
        p = getHanjaDataFromHangul("룡") + p
    }
    else if hangul == "여" {
        p = getHanjaDataFromHangul("려") + p
    }
    else if hangul == "열" {
	p = getHanjaDataFromHangul("렬") + p
    }
    else if hangul == "양" {
        p = p + getHanjaDataFromHangul("량")
    }
    else if hangul == "노" {
        p = getHanjaDataFromHangul("로") + p
    }
    else if hangul == "나" {
        p = p + getHanjaDataFromHangul("라")
    }
    else if hangul == "염" {
        p = p + getHanjaDataFromHangul("렴")
    }
    
    for e in p {
        if (e.0 == hanja) {
            return e
        }
    }
    
    return p[0]
}

